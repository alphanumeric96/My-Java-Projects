public class WhileLoop {
    public static void main(String[] args) {
        int X = 4;
        while (X < 16) {
            System.out.println(X);//Prints The value of X until it is equal to 16
            X++;//Increases the value of X so that the code doesnt run infinitely
        }
    }
}
