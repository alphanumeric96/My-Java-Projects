public class MathDataType {
    public static void main(String[]args){
        int x = 50, y = 51;
        //Getting max value
        System.out.println("Max value is :" + Math.max(x,y));
        //Getting min value
        System.out.println("Min value is :" + Math.min(x,y));
        //Getting Square Root Value
        System.out.println("Square root value of x is :" + Math.sqrt(x));
        System.out.println("Square root value of y is :" + Math.sqrt(y));
    }
}
